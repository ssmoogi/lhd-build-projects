# A variable with an Integer (whole number) value
my_age = 15

# A variable with a String (text) value
my_name = "Sahana"

# We can print out the content of variables in a couple of ways:
print(my_age)
print("Hello " + my_name + "!")
print("Hello {}, you are {} years old.".format(my_name, my_age))
      
